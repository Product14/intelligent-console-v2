export default function SVG({
  iconName,
  className = '',
  height = 24,
  width = 24,
  type,
  style = {},
  name,
  color,
  onClick = () => {},
  fill,
  fillOpacity,
  strokeColor,
  strokeWidth,
}) {
  const Icon = Icons[iconName];
  return Icon ? (
    <Icon
      className={className}
      height={height}
      width={width}
      type={type}
      style={style}
      name={name}
      color={color}
      onClick={onClick}
      fill={fill}
      fillOpacity={fillOpacity}
      strokeColor={strokeColor}
      strokeWidth={strokeWidth}
    />
  ) : null;
}

